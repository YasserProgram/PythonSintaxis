def sumar(numero1, numero2):
    """
    Suma dos números.
    
    Args:
        numero1 (int): Primer número.
        numero2 (int): Segundo número.
    
    Returns:
        int: La suma de numero1 y numero2.
    """
    resultado = numero1 + numero2
    
    return resultado

def restar(numero1, numero2):
    """
    Resta dos números.
    
    Args:
        numero1 (int): Primer número.
        numero2 (int): Segundo número.
    
    Returns:
        int: La resta de numero1 y numero2.
    """
    resultado = numero1 - numero2
    
    return resultado